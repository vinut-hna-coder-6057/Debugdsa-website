import express from "express";

import { protect } from "../middleware/authMiddleware.js";

import {
  testBugRoute,
  createBug,
  getAllBugs,
  getMyBugs,
  getSingleBug,
  deleteBug,
} from "../controllers/bugController.js";

const router = express.Router();

////////////////////////////////////////////////////////////
// TEST ROUTE
////////////////////////////////////////////////////////////

router.get(
  "/test",
  testBugRoute
);

////////////////////////////////////////////////////////////
// CREATE BUG
////////////////////////////////////////////////////////////

router.post(
  "/",
  protect,
  createBug
);

////////////////////////////////////////////////////////////
// GET ALL BUGS
////////////////////////////////////////////////////////////

router.get(
  "/",
  getAllBugs
);

////////////////////////////////////////////////////////////
// GET MY BUGS
////////////////////////////////////////////////////////////

router.get(
  "/my",
  protect,
  getMyBugs
);

////////////////////////////////////////////////////////////
// GET SINGLE BUG
////////////////////////////////////////////////////////////

router.get(
  "/:id",
  getSingleBug
);

////////////////////////////////////////////////////////////
// DELETE BUG
////////////////////////////////////////////////////////////

router.delete(
  "/:id",
  protect,
  deleteBug
);

export default router;