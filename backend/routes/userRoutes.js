import express from "express";

import {
  protect,
  authorize
} from "../middleware/authMiddleware.js";

import {
  testUserRoute,
  getAllUsers,
  getLeaderboard,
  getMyProfile,
  getUserProfile,
  editProfile,
  followUser,
  unfollowUser,
  getSolvedBugs,
  getPointsBreakdown,
  getUpvotesBreakdown,
  getFollowers,
  getFollowing,
  getMyBugSolution
} from "../controllers/userController.js";

const router = express.Router();

////////////////////////////////////////////////////////////
// TEST ROUTE
////////////////////////////////////////////////////////////

router.get(
  "/test",
  testUserRoute
);

////////////////////////////////////////////////////////////
// GET ALL USERS
////////////////////////////////////////////////////////////

router.get(
  "/",
  protect,
  authorize("admin"),
  getAllUsers
);

////////////////////////////////////////////////////////////
// LEADERBOARD
////////////////////////////////////////////////////////////

router.get(
  "/leaderboard",
  getLeaderboard
);

////////////////////////////////////////////////////////////
// MY PROFILE
////////////////////////////////////////////////////////////

router.get(
  "/me",
  protect,
  getMyProfile
);

////////////////////////////////////////////////////////////
// GET PROFILE BY ID
////////////////////////////////////////////////////////////

router.get(
  "/profile/:id",
  getUserProfile
);

////////////////////////////////////////////////////////////
// EDIT PROFILE
////////////////////////////////////////////////////////////

router.put(
  "/edit",
  protect,
  editProfile
);

////////////////////////////////////////////////////////////
// FOLLOW USER
////////////////////////////////////////////////////////////

router.put(
  "/follow/:id",
  protect,
  followUser
);

////////////////////////////////////////////////////////////
// UNFOLLOW USER
////////////////////////////////////////////////////////////

router.put(
  "/unfollow/:id",
  protect,
  unfollowUser
);

////////////////////////////////////////////////////////////
// SOLVED BUGS
////////////////////////////////////////////////////////////

router.get(
  "/bugs",
  protect,
  getSolvedBugs
);

////////////////////////////////////////////////////////////
// POINTS BREAKDOWN
////////////////////////////////////////////////////////////

router.get(
  "/points",
  protect,
  getPointsBreakdown
);

////////////////////////////////////////////////////////////
// UPVOTES BREAKDOWN
////////////////////////////////////////////////////////////

router.get(
  "/upvotes",
  protect,
  getUpvotesBreakdown
);

////////////////////////////////////////////////////////////
// FOLLOWERS
////////////////////////////////////////////////////////////

router.get(
  "/followers",
  protect,
  getFollowers
);

////////////////////////////////////////////////////////////
// FOLLOWING
////////////////////////////////////////////////////////////

router.get(
  "/following",
  protect,
  getFollowing
);

////////////////////////////////////////////////////////////
// CURRENT USER SOLUTION FOR BUG
////////////////////////////////////////////////////////////

router.get(
  "/bug/:bugId/solution",
  protect,
  getMyBugSolution
);

export default router;