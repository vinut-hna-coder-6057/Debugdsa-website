import express from "express";

import { protect } from "../middleware/authMiddleware.js";

import {
  createSolution,
  acceptSolution,
  toggleUpvote,
  deleteSolution,
  getBugSolutions,
  getMySolutions
} from "../controllers/solutionController.js";

const router = express.Router();

////////////////////////////////////////////////////////////
// CREATE SOLUTION
////////////////////////////////////////////////////////////

router.post(
  "/:bugId",
  protect,
  createSolution
);

////////////////////////////////////////////////////////////
// ACCEPT SOLUTION
////////////////////////////////////////////////////////////

router.put(
  "/accept/:solutionId",
  protect,
  acceptSolution
);

////////////////////////////////////////////////////////////
// TOGGLE UPVOTE
////////////////////////////////////////////////////////////

router.put(
  "/upvote/:id",
  protect,
  toggleUpvote
);

////////////////////////////////////////////////////////////
// DELETE SOLUTION
////////////////////////////////////////////////////////////

router.delete(
  "/:id",
  protect,
  deleteSolution
);

////////////////////////////////////////////////////////////
// GET SOLUTIONS FOR BUG
////////////////////////////////////////////////////////////

router.get(
  "/bug/:bugId",
  getBugSolutions
);

////////////////////////////////////////////////////////////
// GET MY SOLUTIONS
////////////////////////////////////////////////////////////

router.get(
  "/my",
  protect,
  getMySolutions
);

export default router;