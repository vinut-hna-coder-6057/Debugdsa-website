import mongoose from "mongoose";

const BattleSchema = new mongoose.Schema({

title:{
type:String,
required:true
},

//////////////////////////////////////////////////
// BUG DATA (ADMIN CREATES DIRECTLY)
//////////////////////////////////////////////////

language:String,

topic:String,

pattern:String,

description:String,

buggyCode:String,

expectedOutput:String,

//////////////////////////////////////////////////
// TIME
//////////////////////////////////////////////////

startTime:{
type:Date,
required:true
},

endTime:{
type:Date,
required:true
},

//////////////////////////////////////////////////
// NEW FIELD: BATTLE STATUS
//////////////////////////////////////////////////

status:{
type:String,
enum:["UPCOMING","LIVE","FINISHED"],
default:"UPCOMING"
},

//////////////////////////////////////////////////
// PARTICIPANTS
//////////////////////////////////////////////////

participants:[
{
type:mongoose.Schema.Types.ObjectId,
ref:"User"
}
],

//////////////////////////////////////////////////
// SUBMISSIONS
//////////////////////////////////////////////////

submissions:[

{
user:{
type:mongoose.Schema.Types.ObjectId,
ref:"User"
},

code:String,

status:{
type:String,
default:"pending"
},

submittedAt:Date,

//////////////////////////////////////////////////
// NEW FIELD (FOR LATE SUBMISSIONS)
//////////////////////////////////////////////////

late:{
type:Boolean,
default:false
}

}

],

//////////////////////////////////////////////////
// NEW FIELD: WINNER
//////////////////////////////////////////////////

winner:{
type:mongoose.Schema.Types.ObjectId,
ref:"User"
}

},{timestamps:true});

//////////////////////////////////////////////////
// SAFE MODEL EXPORT (PREVENTS OVERWRITE ERROR)
//////////////////////////////////////////////////

const Battle =
mongoose.models.Battle || mongoose.model("Battle",BattleSchema);

export default Battle;